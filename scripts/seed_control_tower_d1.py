def read_rows(spec: DatasetSpec) -> list[dict[str, str]]:
    path = DATASET_DIR / spec.file_name
    with path.open("r", encoding="utf-8-sig", newline="") as handle:
        return list(csv.DictReader(handle))


def insert_statements(spec: DatasetSpec, rows: list[dict[str, str]], chunk_size: int) -> list[str]:
    columns = ", ".join(col.db for col in spec.columns)
    statements: list[str] = [f"DELETE FROM {spec.table};"]
    for index in range(0, len(rows), chunk_size):
        chunk = rows[index : index + chunk_size]
        values = []
        for row in chunk:
            values.append("(" + ", ".join(sql_string(col.value(row)) for col in spec.columns) + ")")
        statements.append(f"INSERT OR REPLACE INTO {spec.table} ({columns}) VALUES\n  " + ",\n  ".join(values) + ";")
    return statements


def run_wrangler(database: str, sql_file: Path, remote: bool) -> None:
    npx = shutil.which("npx.cmd") or shutil.which("npx")
    if not npx:
        raise RuntimeError("npx was not found on PATH. Install Node.js or run the script from a shell where npx is available.")
    cmd = [npx, "wrangler", "d1", "execute", database]
    if remote:
        cmd.append("--remote")
    cmd.extend(["--file", str(sql_file)])
    subprocess.run(cmd, cwd=ROOT, check=True)


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--database", default="hvdc-mcp-audit")
    parser.add_argument("--remote", action="store_true")
    parser.add_argument("--chunk-size", type=int, default=200)
    parser.add_argument("--dry-run", action="store_true")
    args = parser.parse_args()

    summary: list[tuple[str, int]] = []
    with tempfile.TemporaryDirectory(prefix="hvdc-d1-seed-") as tmp_dir_raw:
        tmp_dir = Path(tmp_dir_raw)
        migration_target = tmp_dir / MIGRATION.name
        migration_target.write_text(MIGRATION.read_text(encoding="utf-8"), encoding="utf-8")

        if not args.dry_run:
            run_wrangler(args.database, migration_target, args.remote)

        for spec in DATASETS:
            rows = read_rows(spec)
            statements = insert_statements(spec, rows, args.chunk_size)
            sql_path = tmp_dir / f"seed_{spec.table}.sql"
            sql_path.write_text("\n".join(statements) + "\n", encoding="utf-8")
            summary.append((spec.table, len(rows)))
            if not args.dry_run:
                run_wrangler(args.database, sql_path, args.remote)

    for table, count in summary:
        print(f"{table}: {count}")


if __name__ == "__main__":
    main()
